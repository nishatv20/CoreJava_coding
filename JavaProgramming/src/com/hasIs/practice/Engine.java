package com.hasIs.practice;

public class Engine {
	private int eng_no;
	private String eng_type;

	public Engine(int eng_no, String eng_type) {
		this.eng_no = eng_no;
		this.eng_type = eng_type;
	}

	public int geteng_no() {
		return eng_no;
	}

	public String geteng_type() {
		return eng_type;
	}
}
