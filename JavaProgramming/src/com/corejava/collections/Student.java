package com.corejava.collections;

public class Student {
	int rollno;
	String name;
	int per;
	String email;
	int phone;

	public Student(int rollno, String name, int per, String email, int phone) {
		this.rollno = rollno;
		this.name = name;
		this.per = per;
		this.email = email;
		this.phone = phone;
	}
}
